import React, { Component } from 'react';
import './App.css';

const NUM_RECT = 300;

const Rect = ({color}) => {
  const style = {
    width: '100px',
    height: '180px',
    display: 'inline-block',
    backgroundColor: color
  }
  return <div style={style} />
}

class App extends Component {
  constructor(props) {
    super(props);
    const rects = Array(NUM_RECT).fill().map(this.getRandomColor, this)
    this.state = {rects}
    
    setInterval(() => {
      const rects = this.state.rects.slice()
      const randIndex = Math.floor(Math.random() * rects.length)
      rects[randIndex] = this.getRandomColor()
      this.setState({rects})
    },300)
  }
  

  
  getRandomColor() {
    const colorIndex = Math.floor(Math.random() * this.props.allColors.length)
    return this.props.allColors[colorIndex]
  }
 
  render() {
    const rects = this.state.rects.map((color, i) => (
    <Rect key={i} color={color} />
    ))
    return (
     <div className="App">
      {rects}
     </div>
    );
  }
}

